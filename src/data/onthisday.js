/* Событие на сегодня или ближайшее следующее по календарю */
export function pickEvent(now = new Date()) {
  const m = now.getMonth() + 1;
  const d = now.getDate();
  const today = ON_THIS_DAY.filter((e) => e.month === m && e.day === d);
  if (today.length) return { events: today, isToday: true };
  const byOffset = (e) => {
    const diff = (e.month - m) * 31 + (e.day - d);
    return diff > 0 ? diff : diff + 12 * 31;
  };
  const next = [...ON_THIS_DAY].sort((a, b) => byOffset(a) - byOffset(b))[0];
  return { events: [next], isToday: false };
}
